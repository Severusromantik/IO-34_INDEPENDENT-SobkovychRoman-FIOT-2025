// Запитуємо час у форматі ЧЧ:ХХ
const hour = prompt("Будь ласка, введіть час у форматі годин:хвилин (наприклад, 10:30):");

// Виводимо це значення у Console, використовуючи шаблонний рядок
console.log(`Введений час: ${hour}`);

// Перевірка коректності введення та виконання логіки
if (hour === null) {
    // Користувач натиснув "Скасувати"
    alert("Дію скасовано. Введення часу не було здійснено.");
} else if (hour.trim() === "") {
    // Користувач ввів порожній рядок
    alert("Помилка: Введено порожній рядок. Спробуйте знову.");
} else {
    // **Логіка перевірок**
    // Регулярний вираз для перевірки формату ЧЧ:ХХ (00-23:00-59)
    const timeRegex = /^([01]\d|2[0-3]):([0-5]\d)$/;

    if (!timeRegex.test(hour)) {
        // Перевірка формату
        alert(`Помилка: Некоректний формат часу "${hour}". Використовуйте ЧЧ:ХХ (наприклад, 15:05).`);
    } else {
        // Розбиваємо рядок за символом ":" і беремо другий елемент (хвилини)
        const parts = hour.split(':');
        // Перетворюємо рядок хвилин на ціле число
        const minutes = parseInt(parts[1], 10);

        let quarterMessage = "";

        // Задаємо чверті години
        // Година має 60 хвилин. Чверть години = 15 хвилин.
        
        if (minutes >= 0 && minutes <= 15) {
            // 00:00 до 00:15 включно
            quarterMessage = "Перша чверть години (00-15 хвилин)";
        } else if (minutes > 15 && minutes <= 30) {
            // 00:16 до 00:30 включно
            quarterMessage = "Друга чверть години (16-30 хвилин)";
        } else if (minutes > 30 && minutes <= 45) {
            // 00:31 до 00:45 включно
            quarterMessage = "Третя чверть години (31-45 хвилин)";
        } else if (minutes > 45 && minutes <= 59) {
            // 00:46 до 00:59 включно
            quarterMessage = "Четверта чверть години (46-59 хвилин)";
        } else {
             // Помилка на всяк випадок
             console.error("Помилка в логіці визначення хвилин.");
             quarterMessage = "Помилка при визначенні чверті.";
        }

        // **4. Відповідь вивести за допомогою alert()**
        alert(`Введений час ${hour} відноситься до: ${quarterMessage}`);
    }
}